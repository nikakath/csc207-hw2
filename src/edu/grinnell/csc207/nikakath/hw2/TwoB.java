package edu.grinnell.csc207.nikakath.hw2;

public class TwoB {
    /**
     * Compute the average of two integers.  Rounds toward zero if the
     * average is not a whole number.
     */
    public static int average(int left, int right) {
        return (left + right) / 2;
    } // average(int,int)
} // class TwoB
